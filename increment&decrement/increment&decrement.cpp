#include <iostream>
using namespace std;

int main(){
	
	int a = 4;
	int b = 4;
	
	// postincrement
	cout << a << endl;
	a++;
	cout << a << endl << endl;
	
	// preincrement
	cout << a << endl;
	++a;
	cout << a << endl << endl;
	
	// decrement
	cout << b << endl;
	b--;
	cout << b << endl;
	
	return 0;
}
