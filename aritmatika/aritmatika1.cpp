#include <iostream>

using namespace std;

int main() {
	int a = 6;
	int b = 4;
	
	int hasil;
	
//	penjumlahan
	hasil = a + b;
	cout << a << " + " << b << " = " << hasil << endl;
	
//	pengurangan
	hasil = a - b;
	cout << a << " - " << b << " = " << hasil << endl;
	
//	perkalian
	hasil = a * b;
	cout << a << " * " << b << " = " << hasil << endl;
	
//	pembagian
	hasil = a / b;
	cout << a << " / " << b << " = " << hasil << endl;
	
		
	int c = 8;
	int d = 8;
	
//	penjumlahan
	hasil = c + d;
	cout << c << " + " << d << " = " << hasil << endl;
	
//	pengurangan
	hasil = c - d;
	cout << c << " - " << d << " = " << hasil << endl;
	
	
	return 0;
	
}

